################################################################################
##	source								      ##
################################################################################
source	lib/libalx/sh/sysexits.sh;


################################################################################
##	definitions							      ##
################################################################################
ARGC=1;


################################################################################
##	functions							      ##
################################################################################
function change_port()
{
	local	port="$1";

	sed "/ports/{n;s/\".*:/\"${port}:/}"				\
		-i ./etc/docker/swarm/docker-compose.yaml;
}


################################################################################
##	main								      ##
################################################################################
function main()
{
	local	port="$1";

	change_port	"${port}";
}


################################################################################
##	run								      ##
################################################################################
argc=$#;
if [ ${argc} -ne ${ARGC} ]; then
	echo	"Illegal number of parameters (Requires ${MAX_ARGC})";
	exit	${EX_USAGE};
fi

main	"$1";


